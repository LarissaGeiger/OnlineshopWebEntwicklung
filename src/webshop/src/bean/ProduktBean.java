package bean;

public class ProduktBean {
	
	private boolean angebot;
	private int artikelnr;
	private int bildID;
	private int bildschirmgröße;
	private String displaytech;
	private String farbe;
	private int kategorieID;
	private String marke;
	private String modell;
	private String name;
	private double preis;
	
	
	public boolean isAngebot() {
		return angebot;
	}
	public void setAngebot(boolean angebot) {
		this.angebot = angebot;
	}
	public int getArtikelnr() {
		return artikelnr;
	}
	public void setArtikelnr(int artikelnr) {
		this.artikelnr = artikelnr;
	}
	public int getBildID() {
		return bildID;
	}
	public void setBildID(int bildID) {
		this.bildID = bildID;
	}
	public int getBildschirmgröße() {
		return bildschirmgröße;
	}
	public void setBildschirmgröße(int bildschirmgröße) {
		this.bildschirmgröße = bildschirmgröße;
	}
	public String getDisplaytech() {
		return displaytech;
	}
	public void setDisplaytech(String displaytech) {
		this.displaytech = displaytech;
	}
	public String getFarbe() {
		return farbe;
	}
	public void setFarbe(String farbe) {
		this.farbe = farbe;
	}
	public int getKategorieID() {
		return kategorieID;
	}
	public void setKategorieID(int kategorieID) {
		this.kategorieID = kategorieID;
	}
	public String getMarke() {
		return marke;
	}
	public void setMarke(String marke) {
		this.marke = marke;
	}
	public String getModell() {
		return modell;
	}
	public void setModell(String modell) {
		this.modell = modell;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getPreis() {
		return preis;
	}
	public void setPreis(double preis) {
		this.preis = preis;
	}
	
	
	

}
